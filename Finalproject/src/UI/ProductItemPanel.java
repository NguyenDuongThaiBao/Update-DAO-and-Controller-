    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        // TODO add your handling code here:
        int qty = (int) pro_quantity.getValue();
        
        if (qty <= 0) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Quantity must at least 1!", 
            "Quantity error", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
        
        if (placeorderController != null && product != null) {
            placeorderController.addProductToTable(product.getId(), qty); // getID from product then pass ID , qty to admincontroller
            System.out.println("Sending: ID = " + product.getId() + ", Qty = " + qty);
        }
        
        pro_quantity.setValue(0); // reset quantity
    }//GEN-LAST:event_add_btnActionPerformed


