    public ProductItemPanel(Product pro) {
        initComponents();
        this.product = pro;
        NumberFormat currencyFormat = NumberFormat.getCurrencyInstance(new Locale("vi", "VN"));
        String formattedPrice = currencyFormat.format(pro.getPrice());
        pro_name.setText(pro.getName());
        pro_price.setText(formattedPrice);
        ImageIcon imgIcon = new ImageIcon(pro.getImage());
        Image img = imgIcon.getImage();

        int width = pro_img.getWidth();
        int height = pro_img.getHeight();

        if (width == 0 || height == 0) {
            width = 180;
            height = 160;
        }

        Image scaled = img.getScaledInstance(width, height, Image.SCALE_SMOOTH);
        pro_img.setIcon(new ImageIcon(scaled));
        
        if ("Unavailable".equalsIgnoreCase(pro.getStatus()) || pro.getStock() <= 0) {
        
            items_panel.setBackground(java.awt.Color.GRAY);
            jPanel2.setBackground(java.awt.Color.GRAY);

            pro_name.setForeground(java.awt.Color.LIGHT_GRAY);
            pro_price.setForeground(java.awt.Color.LIGHT_GRAY);

            add_btn.setEnabled(false);
            add_btn.setText("OOS");
            add_btn.setBackground(java.awt.Color.DARK_GRAY);

            pro_quantity.setEnabled(false);

        } else {
            items_panel.setBackground(new java.awt.Color(255, 51, 51));
            jPanel2.setBackground(new java.awt.Color(204, 204, 204));

            add_btn.setEnabled(true);
            add_btn.setText("Add");
            add_btn.setBackground(new java.awt.Color(255, 51, 51));

            pro_quantity.setEnabled(true);
        }
    }

    
