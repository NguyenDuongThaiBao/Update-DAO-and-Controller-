    private void staff_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_staff_btnActionPerformed
        // TODO add your handling code here:
        gethome_btn(staff_btn);
        tab_menu.removeAll();
        tab_menu.setLayout(new BorderLayout());
        tab_menu.add(staff_panel, BorderLayout.CENTER);
        
        javax.swing.JScrollPane scroll = new javax.swing.JScrollPane(staff_panel);
    
        // Tắt viền và tăng tốc độ cuộn cho mượt
        scroll.setBorder(null);
        scroll.getVerticalScrollBar().setUnitIncrement(16);

        // Add thanh cuộn vào tab_menu
        tab_menu.add(scroll, java.awt.BorderLayout.CENTER);
        
        tab_menu.revalidate();
        tab_menu.repaint();
        Model.User user = Model.Session.getCurrentUser();
        if (user != null) {
            welcome_status.setText("Welcome " + user.getUsername());
            staff_panel.lockSystem(); 
            staff_control.loadUserProfile(user);
        }
    }//GEN-LAST:event_staff_btnActionPerformed

    private void logout_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logout_btnActionPerformed
        // TODO add your handling code here:
        gethome_btn(logout_btn);
        int choice = javax.swing.JOptionPane.showConfirmDialog(
            this, 
            "Do you want to log out?", 
            "Logout confirm", 
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (choice == javax.swing.JOptionPane.YES_OPTION) {
        
            Model.Session.logout();

            this.dispose();

            new Loginform().setVisible(true); 
        }
    }//GEN-LAST:event_logout_btnActionPerformed

